int main()
{
	//要素数を定義する為。
	const int ELEMENT = 5;

	//int型かつ指定された数の要素を持つ配列を	2つ用意する為。
	int a[ELEMENT];

	int b[ELEMENT];

	//値を出力する為。
	cout << "◆a配列の値\n";

	//要素数分繰り返す為。
	for (int i = 0; i < ELEMENT; i++){

		//要素に値を設定する為。
		a[i] = i + 1;

		//値を出力する為。
		cout << "a[" << i << "]：" << a[i] << "\n";
	}

	//要素数分繰り返す為。
	for (int j = 0; j < ELEMENT; j++){

		//要素に値を設定する為。
		b[j] = a[ELEMENT - j - 1];

	}

	//値を出力する為。
	cout << "\n◆b配列の値\n";

	//要素数分繰り返す為。
	for (int k = 0; k < ELEMENT; k++){

		//値を出力する為。
		cout << "b[" << k << "]：" << b[k] << "\n";
	}
}
