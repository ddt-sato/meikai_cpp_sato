//　　　　　　　　：-2147483648～2147483647の値が取得されてしまった為、
//　　　　　　　　：当演習ではSHRT_MIN、SHRT_MAXを使用する。
//============================================================================
int main() {

	//画面に表示させる為。
	cout << "8進数　10進数　16進数\n";

	//以下の処理を繰返し実行する為。
	for (int i = SHRT_MIN; i <= SHRT_MAX ; i++)
	{
		//8進数で画面に表示させる為。
		cout << oct << i << " ";

		//10進数で画面に表示させる為。
		cout << dec << i << " ";

		//16進数で画面に表示させる為。
		cout << hex << i << "\n";
	}
}
